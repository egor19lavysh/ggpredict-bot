async def create_mock_data():
    """Создание 30 тестовых предсказаний"""
    repository = PredictionRepository()
    
    for text in predictions_text:
        chance = random.uniform(50, 70)  # Случайный шанс от 50 до 70
        
        await repository.create(
            text=text,
            chance=round(chance, 2),
            image=None,
            status=StatusEnum.WITHOUT_PRIZE
        )
        print(f"✅ Создано предсказание: '{text}' с шансом {round(chance, 2)}%")
    
    print(f"\n✅ Успешно создано {len(predictions_text)} предсказаний!")


if __name__ == "__main__":
    asyncio.run(create_mock_data())
